/**
 * A controller which delegates requests to appropriate dispatchers
 * and fowards requests to the view handler.
 *
 * @package    Piece_Unity
 * @copyright  2006-2009 KUBO Atsuhiro <kubo@iteman.jp>
 * @license    http://www.opensource.org/licenses/bsd-license.php  New BSD License
 * @version    Release: @package_version@
 * @since      Class available since Release 0.4.0
 */
class Piece_Unity_Plugin_Controller extends Piece_Unity_Plugin_Common
{

    // {{{ properties

    /**#@+
     * @access public
     */

    /**#@-*/

    /**#@+
     * @access protected
     */

    /**#@-*/

    /**#@+
     * @access private
     */

    /**#@-*/

    /**#@+
     * @access public
     */

    // }}}
    // {{{ invoke()

    /**
     * Invokes the plugin specific code.
     */
    public function invoke()
    {
        if (is_null($this->context->getView())) {
            $viewString = $this->getExtension('dispatcher')->invoke();

            if (is_null($this->context->getView())) {
                $this->context->setView($viewString);
            }
        }

        Piece_Unity_Plugin_Factory::factory('Dispatcher_Continuation')->publish();
        $this->getExtension('view')->invoke();
    }

    /**#@-*/

    /**#@+
     * @access protected
     */

    // }}}
    // {{{ initialize()

    /**
     * Defines and initializes extension points and configuration points.
     *
     * @since Method available since Release 0.6.0
     */
    protected function initialize()
    {
        $this->addExtensionPoint('dispatcher', 'Dispatcher_Continuation');
        $this->addExtensionPoint('view', 'View');
    }

    /**#@-*/

    /**#@+
     * @access private
     */

    /**#@-*/

    // }}}
}

// }}}

